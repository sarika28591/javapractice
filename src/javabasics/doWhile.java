package javabasics;

public class doWhile {
	public static void main(String args[]) {
			int x = 10;
		     do {
			System.out.println("X number is " + x);
			x--;
		     } while(x >= 0);
		     
		     int y = 20;
		     while(y >= 0) {
		    	 System.out.println("Y number is " + y);
		    	 y--;
		     }
		   
		     }
}
