package javabasics;

public class Foreachloop {
	public static void main(String args[]) {
		String[] celebrities = {"asdasdsad", "dwerwer" ,"sdsdde"};
		for(String name:celebrities) {
			System.out.print(name);
			System.out.print(",");
		}
			System.out.print("\n");
		int[] age = {20 , 30 , 40 , 50};
		for(int x:age) {
			System.out.print(x);
			System.out.print(",");
			
		}
	}
	}

